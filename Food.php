Emulsifiers in food can perform a variety of functions, the most important involves the reductions of surface tension in oil-water interface, interactions with starch and protein components and modification of the crystallisation of fats and oils. An emulsion is basically the dispersion of small droplets of one insoluble substance within another (oil in water or water in oil). Emulsions are usually the first type of mixture considered when talking about food emulsifiers. In food applications, surfactants exert in variety of specific functions, other effects can be obtained, many of these uses may overlap in any given application.
