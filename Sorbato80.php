            <ol>
              <li class="Elementos--Listas">A pesar de contener un ácido graso (ácido oleico), no tiene una estructura de hidrocarburo puro, lo que significa que se comporta de manera diferente a otros compuestos lipídicos más simples.</li>
            </ol>
          </li>
        </ol>

