    <h2 class="Tipografia--general letranegro General--Cuerpo"><b style="color:#FFCCCC">Mono Oleato de Sorbitano | Sorbato 80 | Span 80</b></h2>

    <p style="margin-top:0px;" class="Tipografia--general letranegro General--Cuerpo">El <b>Mono Oleato de Sorbitano</b> es un éster de sorbitano (un derivado del azúcar) y el ácido oleico, que es un ácido graso insaturado que se encuentra comúnmente en los aceites vegetales como el aceite de oliva. Este compuesto se utiliza principalmente como un emulsionante, lo que significa que ayuda a mezclar sustancias que normalmente no se mezclarían bien, como el aceite y el agua.</p>

    <p class="Tipografia--general letranegro General--Cuerpo" style="margin-top:50px;">El Mono Oleato de Sorbitano tiene diversas aplicaciones, principalmente en la industria alimentaria, farmacéutica y cosmética.</p>

    <h2 class="Tipografia--general letranegro General--Cuerpo" style="margin-top:50px;"><b style="color:#FFCCCC">Características Físicas</b></h2>

    <ol>
        <li class="Elementos--Listas"><b>Apariencia:</b>
            <ol>
            <li class="Elementos--Listas">Es un líquido espeso o una pasta viscosa, generalmente de color amarillo a amarillo pálido.</li>
            </ol>
        </li>
        </ol>

        <ol>
            <li class="Elementos--Listas" style="margin-top:50px;"><b>Olor:</b>
                <ol>
                    <li class="Elementos--Listas">Tiene un olor leve, ligeramente graso o neutro, que no suele ser muy perceptible en las concentraciones comunes.</li>
                </ol>
            </li>
        </ol>

        <ol>
          <li class="Elementos--Listas" style="margin-top:50px;"><b>Viscosidad:</b>
            <ol>
              <li class="Elementos--Listas">Posee una alta viscosidad, lo que lo hace espeso y denso. La viscosidad puede variar según la concentración y la temperatura.</li>
            </ol>
          </li>
        </ol>

        <ol>
          <li class="Elementos--Listas" style="margin-top:50px;"><b>Punto de fusión:</b>
            <ol>
              <li class="Elementos--Listas">El punto de fusión es relativamente bajo, alrededor de -5 °C a 0 °C, por lo que a temperaturas normales suele ser un líquido o gel.</li>
            </ol>
          </li>
        </ol>

        <ol>
          <li class="Elementos--Listas" style="margin-top:50px;"><b>Solubilidad:</b>
            <ol>
              <li class="Elementos--Listas">Es liposoluble (soluble en aceites y grasas), pero tiene baja solubilidad en agua. Esto lo hace adecuado para su uso en emulsiones oleosas y productos que contienen ingredientes grasos.</li>
            </ol>
          </li>
        </ol>

        <ol>
          <li class="Elementos--Listas" style="margin-top:50px;"><b>Densidad:</b>
            <ol>
              <li class="Elementos--Listas">La densidad es alrededor de 0.96 - 1.0 g/cm³ a temperatura ambiente.</li>
            </ol>
          </li>
        </ol>

        <ol>
          <li class="Elementos--Listas" style="margin-top:50px;"><b>Estabilidad térmica:</b>
            <ol>
              <li class="Elementos--Listas">El Mono Oleato de Sorbitano es bastante estable a temperaturas moderadas, aunque su estabilidad puede verse afectada a temperaturas extremadamente altas.</li>
            </ol>
          </li>
        </ol>

        <ol>
          <li class="Elementos--Listas" style="margin-top:50px;"><b>Índice de refracción:</b>
            <ol>
              <li class="Elementos--Listas">Su índice de refracción es alrededor de 1.46 a temperatura ambiente.</li>
            </ol>
          </li>
        </ol>


        <h2 class="Tipografia--general letranegro General--Cuerpo"><b style="color:#FFCCCC;">Características Químicas</b></h2>

        <ol>
          <li class="Elementos--Listas" style="margin-top:0px;"><b>Composición química:</b>
            <ol>
              <l class="Elementos--Listas">El Mono Oleato de Sorbitano es un éster formado por la reacción de sorbitano (un alcohol derivado del sorbitol) y ácido oleico (un ácido graso insaturado, común en aceites vegetales como el de oliva). Su fórmula química es C18H34O6.</li>
            </ol>
          </li>
        </ol>

        <ol>
          <li class="Elementos--Listas" style="margin-top:50px;"><b>Grupos funcionales:</b>
            <ol>
              <l class="Elementos--Listas">Contiene un grupo éster (-COO-) que es responsable de sus propiedades emulsionantes.</li>
              <li class="Elementos--Listas">En su estructura también contiene un grupo hidroxilo (-OH), propio del sorbitano, lo que le permite interaccionar con el agua en ciertas condiciones.</li>
            </ol>
          </li>
        </ol>

        <ol>
          <li class="Elementos--Listas" style="margin-top:50px;"><b>Acidez:</b>
            <ol>
              <li class="Elementos--Listas">Tiene una acidez baja, lo que lo hace adecuado para su uso en productos alimentarios y cosméticos sin alterar demasiado el pH de las formulaciones.</li>
            </ol>
          </li>
        </ol>

        <ol>
          <li class="Elementos--Listas" style="margin-top:50px;"><b>Reactividad:</b>
            <ol>
              <li class="Elementos--Listas">Debido a su estructura química, el Mono Oleato de Sorbitano es relativamente estable a condiciones normales de almacenamiento y uso. Sin embargo, su estabilidad puede verse afectada por factores como la exposición a temperaturas elevadas o la presencia de agentes oxidantes</li>
            </ol>
          </li>
        </ol>


