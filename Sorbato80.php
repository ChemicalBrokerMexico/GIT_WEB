          <li class="Elementos--Listas" style="margin-top:50px;"><b>Baja toxicidad:</b>
            <ol>
              <li class="Elementos--Listas">En términos de reactividad química y toxicidad, el Mono Oleato de Sorbitano es considerado seguro en las concentraciones utilizadas en la industria alimentaria y cosmética. Su toxicidad es mínima, y es considerado biodegradable.</li>
            </ol>
          </li>
        </ol>
        

