              </ol>
            </li>
            <li class="Elementos--Listas" style="margin-top:30px;">
              Organoclorados
              <ol style="margin-top:30px;">
                <li style="list-style-type:disc;">Esta familia química hace referencia a un amplio grupo de compuestos químicos orgánicos que contiene cloro, además de otros elementos químicos, muchos insecticidas poderosos y efectivos son compuestos organoclorados.</li>
              </ol>
            </li>
